export function getCustomersByCountry() {
  const byCountry: Record<string, { country: string; totalOrders: number; customerCount: number }> = {};

  for (const customer of customers) {
    if (!byCountry[customer.country]) {
      byCountry[customer.country] = {
        country: customer.country,
        totalOrders: 0,
        customerCount: 0,
      };
    }
    byCountry[customer.country].totalOrders += customer.orderCount;
    byCountry[customer.country].customerCount += 1;
  }

  return Object.values(byCountry).sort((a, b) => b.totalOrders - a.totalOrders);
}

export function getTotalCustomerStats() {
  const totalOrders = customers.reduce((sum, c) => sum + c.orderCount, 0);
  const totalCustomers = customers.length;
  const countries = new Set(customers.map((c) => c.country)).size;

  return {
    totalOrders,
    totalCustomers,
    countries,
    avgOrdersPerCustomer: Math.round(totalOrders / totalCustomers),
  };
}
